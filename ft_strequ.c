#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int i;
	int j;

	i = 0;
	j = 1;
	if (s1 != NULL && s2 != NULL)
	{
		while ((s1[i] != '\0' || s2[i] != '\0') && (j == 1))
		{
			if (s1[i] != s2[i])
				j = 0;
			i++;
		}
	}
	return (j);
}
