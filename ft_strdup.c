#include "libft.h"

char	*ft_strdup(const char *src)
{
	int i;
	char*dup;

	dup = (char*)malloc(sizeof(char) * ft_strlen(src) + 1);
	if (dup == NULL)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
